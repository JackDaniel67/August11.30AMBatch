package org.base;

public class EmployeeInfo {

	private int empId;
	private String username;

	public void setEmpId(int id) {
		this.empId = id;

	}

	public int getEmpId() {
		return empId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	
}